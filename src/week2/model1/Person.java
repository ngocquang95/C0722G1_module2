package week2.model1;

public abstract class Person {
    public void run() {
        System.out.println("Person Run");
    }

    public void run(int x) {
        System.out.println("Person Run");
    }

    public void run(double x) {
        System.out.println("Person Run");
    }

    public void run(double x, double y) {
        System.out.println("Person Run");
    }
}
